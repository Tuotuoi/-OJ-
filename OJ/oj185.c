#include<stdio.h>
long long f[1000];
int main() {
    int a = 0;
    scanf("%d",&a);
    f[0] = 0;
    f[1] = 1;
    f[2] = 1;
    for(int i = 3; i <= a; i++) {
        f[i] = f[i - 1] + f[i - 2];
    }
    printf("%lld",f[a]);
    
}
