#include<stdio.h>
long long a[100005] = {0, 1, 1};
int main() {
    int n;
    scanf("%d",n);
    for(int i = 3 ; i <= n; i++) {
        a[i] = a[i - 1] + a[i - 2];
        a[i] %= 1000000007;
    }
    printf("%lld", a[n]);
    return 0;
}
